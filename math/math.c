#include "cub3D.h"

double sign(double n)
{
	return ((n >= 0) - (n < 0));
}

int max(int a, int b)
{
	return (a * (a >= b) + (a < b) * b );
}

int min(int a, int b)
{
	return (a * (a <= b) + (a > b) * b );
}
